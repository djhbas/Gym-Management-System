// For searching a specific record from database
    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
         try {
            String SEARCH = s.getText();

            con = DriverManager.getConnection("jdbc:derby://localhost:1527/members", "usn", "123");
            st = con.createStatement();

            String view = "SELECT * FROM Members WHERE id = " + SEARCH;
            rs = st.executeQuery(view);

            if (rs.next() == false) {
                JOptionPane.showMessageDialog(this, "Data not found!");
            } else {
                String ID = rs.getString("id");
                String FNAME = rs.getString("fname");
                String LNAME = rs.getString("lname");
                String AGE = rs.getString("age");
                String CONTACT = rs.getString("contact");
                String SUB = rs.getString("subscription");
                String AMOUNT = rs.getString("amount");
                String STATUS = rs.getString("status");

                String data[] = {ID, FNAME, LNAME, AGE, CONTACT, SUB, AMOUNT, STATUS};
                DefaultTableModel tbl = (DefaultTableModel) subscriptionTable.getModel();
                tbl.setRowCount(0);
                tbl.addRow(data);
            }
            con.close();
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_searchActionPerformed
    // To update the payment status of the record to database
    public void paymentStat(double change) {

        String update = "UPDATE Members SET status='PAID' WHERE id=" + id.getText();
        try {
            
            st = con.createStatement();
            st.executeUpdate(update);
            JOptionPane.showMessageDialog(this, "Your change is " + change + "0");
            money.setText("");

        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }
