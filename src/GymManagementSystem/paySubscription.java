// To add payment subscriptions for the records to database
    private void payActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_payActionPerformed

        try {
            con = DriverManager.getConnection("jdbc:derby://localhost:1527/members", "usn", "123");
            st = con.createStatement();

            if (money.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Enter your money first!", 
                "Error", JOptionPane.ERROR_MESSAGE);
            } else {

                String ID = id.getText();
                String view = "SELECT * FROM Members WHERE id = " + ID;
                rs = st.executeQuery(view);

                if (rs.next()) {
                    String stat = rs.getString("status");

                    if (stat.equals("PAID")) {
                        JOptionPane.showMessageDialog(this, "This record has already paid!", 
                        "Error", JOptionPane.ERROR_MESSAGE);
                        money.setText("");
                    } else {
                        double x = Double.parseDouble(amount.getText());
                        double y = Double.parseDouble(money.getText());

                        if (y < x) {
                            JOptionPane.showMessageDialog(this, "Money is not enough! Please try again", 
                            "Error", JOptionPane.ERROR_MESSAGE);
                        } else {
                            double z = y - x;
                            paymentStat(z);
                            DefaultTableModel tbl = (DefaultTableModel) subscriptionTable.getModel();
                            tbl.setRowCount(0);
                            updateTable();
                        }
                    }
                }
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_payActionPerformed
